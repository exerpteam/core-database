        RENEWAL_POLICY_OVERRIDE, S.ADMINFEE_INVOICELINE_SUBID AS 
        ADMINFEE_INVOICELINE_SUBID, S.CAMPAIGN_CODE_ID AS 
        CAMPAIGN_CODE_ID, S.IS_PRICE_UPDATE_EXCLUDED AS 
        IS_PRICE_UPDATE_EXCLUDED, PR.NAME AS NAME, PR.GLOBALID AS 
        GLOBAL_ID 
    from SUBSCRIPTIONS AS S LEFT 
    join PERSONS AS P ON (P.CENTER = S.OWNER_CENTER AND P.ID = S.OWNER_ID) 
        LEFT 
    join PRODUCTS AS PR ON (PR.CENTER = S.SUBSCRIPTIONTYPE_CENTER AND PR.ID 
        = S.SUBSCRIPTIONTYPE_ID) LEFT 
    join PERSONS AS CURPERS ON (CURPERS.CENTER = P.CURRENT_PERSON_CENTER 
        AND CURPERS.ID = P.CURRENT_PERSON_ID) 
    where P.CENTER IN (103,
