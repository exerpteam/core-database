SELECT

ar.*




 FROM ACCOUNT_TRANS act

INNER JOIN AGGREGATED_TRANSACTIONS agr
ON act.AGGREGATED_TRANSACTION_CENTER = agr.CENTER
AND
act.AGGREGATED_TRANSACTION_ID = agr.ID
INNER JOIN CENTERS c
ON c.ID = agr.CENTER


INNER JOIN AR_TRANS ar
ON ar.REF_CENTER = act.CENTER
AND ar.REF_ID = act.ID
AND ar.REF_SUBID = act.SUBID












WHERE EXTRACT(month FROM agr.BOOK_DATE) = 5 
--AND c.COUNTRY = 'IT'
AND act.CENTER = 105
AND ACT.TRANS_TYPE = 2
AND ar.PAYREQ_SPEC_CENTER IS  NULL







