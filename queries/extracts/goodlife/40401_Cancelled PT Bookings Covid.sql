    pa.participant_center||'p'||pa.participant_id                   AS "Participant ID",
    su.person_center||'p'||su.person_id                             AS "Staff ID",
    part.fullname as "Participant Name",
    staff.fullname as "Staff Name"
FROM
    bookings b
LEFT JOIN
    goodlife.staff_usage su
ON
    su.booking_center = b.center
aND su.booking_id = b.id
LEFT JOIN
    goodlife.participations pa
ON
    pa.booking_center = b.center
AND pa.booking_id = b.id
left JOIN
    goodlife.activity a
ON
    a.id = b.activity
left JOIN
    persons part
ON
    part.center = pa.participant_center
AND part.id = pa.participant_id
left JOIN
    goodlife.persons staff
ON
    staff.center = su.person_center
AND staff.id = su.person_id
