SELECT JOBNAME, TO_DATE('01-01-1970','dd-mm-yyyy ') + FLOOR(1/12 + COMPLETIONTIME/(24*3600*1000)) as COMPLETION_TIME , SUM(ERRORS) , SUM(FATALERRORS),
COUNT(*) as Executions
FROM BATCHLOGS 
WHERE
TO_DATE('01-01-1970','dd-mm-yyyy ') + COMPLETIONTIME/(24*3600*1000) BETWEEN :from_date
AND 
:to_date +1
AND JOBNAME NOT LIKE  '%Booking%' 
GROUP BY JOBNAME, TO_DATE('01-01-1970','dd-mm-yyyy ') + FLOOR(1/12 +COMPLETIONTIME/(24*3600*1000))
