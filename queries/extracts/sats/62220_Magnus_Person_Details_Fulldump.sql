SELECT
	PERSON_ID,
	REPLACE(REPLACE(ADDRESS1,'"',''),';',',') AS ADDRESS1,
	REPLACE(REPLACE(ADDRESS2,'"',''),';',',') AS ADDRESS2,
	REPLACE(REPLACE(ADDRESS3,'"',''),';',',') AS ADDRESS3,

	REPLACE(REPLACE(WORK_PHONE,'"',''),';',',') AS WORK_PHONE,
	REPLACE(REPLACE(MOBILE_PHONE,'"',''),';',',') AS MOBILE_PHONE,
	REPLACE(REPLACE(HOME_PHONE,'"',''),';',',') AS HOME_PHONE,
	REPLACE(REPLACE(EMAIL,'"',''),';',',') AS EMAIL,

	CENTER_ID,
	ETS
FROM
    BI_PERSON_DETAILS biview
